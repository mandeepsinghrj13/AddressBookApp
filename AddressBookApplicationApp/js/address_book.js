class Contact {
  get id() {
    return this._id;
  }

  set id(id) {
    this._id = id;
  }

  get firstName() {
    return this._firstName;
  }

  set firstName(firstName) {
    this._firstName = firstName;
  }

  get lastName() {
    return this._lastName;
  }

  set lastName(lastName) {
    this._lastName = lastName;
  }

  get address() {
    return this._address;
  }

  set address(address) {
    this._address = address;
  }

  get city() {
    return this._city;
  }

  set city(city) {
    this._city = city;
  }

  get state() {
    return this._state;
  }

  set state(state) {
    this._state = state;
  }

  get zip() {
    return this._zip;
  }

  set zip(zip) {
    this._zip = zip;
  }

  get phone() {
    return this._phone;
  }

  set phone(phone) {
    this._phone = phone;
  }

  get email() {
    return this._email;
  }

  set email(email) {
    this._email = email;
  }
}
